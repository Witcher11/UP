//Използвайте оператор switch като според въведен месец от годината (цяло число) да се изведе кой сезон е.
#include <iostream>
using namespace std;
int main(){

int month;
cout<<" Enter month: ";
cin>>month;
switch(month){

case 1: cout<<" Winter "<<endl;break;

case 2: cout<<" Winter"<<endl;break;

case 3: cout<<" Spring"<<endl;break;

case 4: cout<<" Spring"<<endl;break;

case 5: cout<<" Spring"<<endl;break;

case 6: cout<<" Summer"<<endl;break;

case 7: cout<<" Summer"<<endl;break;

case 8: cout<<" Summer"<<endl;break;

case 9: cout<<" Fall"<<endl;break;

case 10: cout<<" Fall"<<endl;break;

case 11: cout<<" Fall"<<endl;break;

case 12: cout<<" Winter"<<endl;break;

default:cout<<" No  such month" ;




return 0;

}






































}
